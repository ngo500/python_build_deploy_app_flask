def summation(x, y):
    """
    Take x and y, and return their sum
    """
    result = x + y
    return result

def subtraction(x, y):
    """
    Take x and y, and return their difference
    """
    result = x - y
    return result

def multiplcation(x, y):
    """
    Take x and y, and return their product
    """
    result = x * y
    return result
